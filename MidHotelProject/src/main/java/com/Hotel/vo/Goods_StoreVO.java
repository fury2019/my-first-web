package com.Hotel.vo;

public class Goods_StoreVO {
	private String store_no;
	private int store_price;
	private int store_amt;
	private String goods_id;
	private String goods_date;
	
	public String getStore_no() {
		return store_no;
	}
	public void setStore_no(String store_no) {
		this.store_no = store_no;
	}
	public int getStore_price() {
		return store_price;
	}
	public void setStore_price(int store_price) {
		this.store_price = store_price;
	}
	public int getStore_amt() {
		return store_amt;
	}
	public void setStore_amt(int store_amt) {
		this.store_amt = store_amt;
	}
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public String getGoods_date() {
		return goods_date;
	}
	public void setGoods_date(String goods_date) {
		this.goods_date = goods_date;
	}
	
	@Override
	public String toString() {
		return "Goods_StoreVO [store_no=" + store_no + ", store_price=" + store_price + ", store_amt=" + store_amt
				+ ", goods_id=" + goods_id + ", goods_date=" + goods_date + "]";
	}
	
	
}
