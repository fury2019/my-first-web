package com.Hotel.vo;

public class Room_PaymentVO {

	private String r_res_no;
	private int p_use_point;
	private String coupon_no;
	private int p_earn_point;
	private int p_tot_price;
	private int p_price;
	private String p_date;
	public String getR_res_no() {
		return r_res_no;
	}
	public void setR_res_no(String r_res_no) {
		this.r_res_no = r_res_no;
	}
	public int getP_use_point() {
		return p_use_point;
	}
	public void setP_use_point(int p_use_point) {
		this.p_use_point = p_use_point;
	}
	public String getCoupon_no() {
		return coupon_no;
	}
	public void setCoupon_no(String coupon_no) {
		this.coupon_no = coupon_no;
	}
	public int getP_earn_point() {
		return p_earn_point;
	}
	public void setP_earn_point(int p_earn_point) {
		this.p_earn_point = p_earn_point;
	}
	public int getP_tot_price() {
		return p_tot_price;
	}
	public void setP_tot_price(int p_tot_price) {
		this.p_tot_price = p_tot_price;
	}
	public int getP_price() {
		return p_price;
	}
	public void setP_price(int p_price) {
		this.p_price = p_price;
	}
	public String getP_date() {
		return p_date;
	}
	public void setP_date(String p_date) {
		this.p_date = p_date;
	}
	
	@Override
	public String toString() {
		return "Room_PaymentVO [r_res_no=" + r_res_no + ", p_use_point=" + p_use_point + ", coupon_no=" + coupon_no
				+ ", p_earn_point=" + p_earn_point + ", p_tot_price=" + p_tot_price + ", p_price=" + p_price
				+ ", p_date=" + p_date + "]";
	}
	
	
}
